class RNGX {
public:
    std::vector<std::unique_ptr<RNGX>> level1;
    RNGX() {
        std::random_device rd;
        gen_ = std::mt19937(rd());
    }

    int generateUnique(std::set<int>& usedNumbers, int min, int max) {
        std::uniform_int_distribution<> dist(min, max);
        int num;
        do {
            num = dist(gen_);
        } while (usedNumbers.count(num) > 0);
        usedNumbers.insert(num);
        return num;
    }
    std::vector<std::unique_ptr<RNGX>> children;
     /*这里是确保了每一组数字的生成都能完美无瑕的让一组数字里面的所有数字都不会重复                                           
       但其实这里似乎有点没多大作用 这里是从其他写的代码抄过来的 也懒得删除了                                           
       但如果想真正用上这个数字不会重复，那么可以复制一下下面的rngxg的代码                                          
       这个文件可作为头文件使用，也可作为你的代码的一部分来使用，我是不管的                                          
       一般的话，如果你想使用这个一组数字里面所有的数字都不会重复的功能，推荐作为你代码的一部分                                          
       当然你暴力改头文件也不是不行 awa*/
    void create_children(std::vector<std::unique_ptr<RNGX>>& parents, int num_children);
    void rngxl();
    int rngxg(int min, int max);

private:
    std::mt19937 gen_;

};
void RNGX::create_children(std::vector<std::unique_ptr<RNGX>>& parents, int num_children) {
    for (auto& parent : parents) {
        for (int i = 0; i < num_children; ++i) {
            parent->children.push_back(std::make_unique<RNGX>());
        }
    }
}

